export default class calculator {
    constructor (){}

    add(a, b) {
        let c = a + b;
        return c;
    }
    divide(a, b) {
        let c = a / b;
        return c;
    }
    sub(a, b) {
        let c = a - b;
        return c;
    }
    multi(a, b) {
        let c = a * b;
        return c;
    }
}